    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        String u = jTextFieldUsername.getText();
        String p = jTextFieldPassword.getText();
        if(u.equals("") && p.equals("")) {
            JOptionPane.showMessageDialog(this, "Username atau password kosong","Warning",HEIGHT);
        } else {
            if(isUsername(username,u)) {
                int i = username.indexOf(u);
                if(password.get(i).equals(p)) {
                    this.dispose();
                    new programJava_4(nama.get(i), username.get(i), password.get(i)).setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(this, "Password Salah","Warning",HEIGHT);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Username tidak ada","Warning",HEIGHT);
            }
        }
        
    }//GEN-LAST:event_jButtonLoginActionPerformed

