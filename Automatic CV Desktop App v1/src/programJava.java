    private void jButtonCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCetakActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("PDF Files","pdf");
        fc.setFileFilter(filter);
        fc.showSaveDialog(this);
        System.out.println("Nama foto : "+mProf.foto);
        String loc = fc.getSelectedFile().getAbsolutePath() + ".pdf";
        try{
            try {
                writer = PdfWriter.getInstance(document, new FileOutputStream(loc));
                image = Image.getInstance(mProf.foto);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(programJava.class.getName()).log(Level.SEVERE, null, ex);
            } catch (BadElementException ex) {
                Logger.getLogger(programJava.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(programJava.class.getName()).log(Level.SEVERE, null, ex);
            }
            document.open();
                Font font1 = new Font();
                    font1.setStyle(Font.BOLD);
                    font1.setSize(25);
                Font font2 = new Font();
                    font2.setStyle(Font.NORMAL);
                    font2.setSize(15);
               
      
                input("CV Otomatis",font1,Element.ALIGN_CENTER);
                document.add(image);
                input("--------------------------------------------------------------------------------------------------------",font2,Element.ALIGN_JUSTIFIED);
                input("Nama Lengkap :  " +mProf.nama,font2,Element.ALIGN_LEFT);
                input("Jenis Kelamin   :  " +mProf.jenKel,font2,Element.ALIGN_LEFT);
                input("NIM                   :  " +mProf.nim,font2,Element.ALIGN_LEFT);
                input("Tempat             :  " +mProf.tempatLahir,font2,Element.ALIGN_LEFT);
                input("Tanggal Lahir   :  " +mProf.tanggalLahir.get(0)+" "+mProf.tanggalLahir.get(1)+
                        " "+mProf.tanggalLahir.get(2),font2,Element.ALIGN_LEFT);
                input("Agama              :  " +mProf.agama,font2,Element.ALIGN_LEFT);
                input("Alamat Asal      :  " +mProf.alamatAsal,font2,Element.ALIGN_LEFT);
                input("Alamat Domisili :  " +mProf.alamatDomisili,font2,Element.ALIGN_LEFT);
                input("--------------------------------------------------------------------------------------------------------",font2,Element.ALIGN_JUSTIFIED);
                input("PENGALAMAN ORGANISASI",font1,Element.ALIGN_CENTER);
                input(" ",font1,Element.ALIGN_CENTER);
                for (int i=0;i<mOrg.nama.size();i++) {
                    input((i+1)+". Nama    :  " +mOrg.nama.get(i),font2,Element.ALIGN_LEFT);
                    input("    Jabatan :  " +mOrg.jabatan.get(i),font2,Element.ALIGN_LEFT);
                    input("    Tahun    :  " +mOrg.tahunMulai.get(i)+" - "+mOrg.tahunSelesai.get(i),font2,Element.ALIGN_LEFT);
                }
                input(" ",font1,Element.ALIGN_CENTER);
                input("--------------------------------------------------------------------------------------------------------",font2,Element.ALIGN_JUSTIFIED);
                input("PENGALAMAN KEPANITIAAN",font1,Element.ALIGN_CENTER);
                input(" ",font1,Element.ALIGN_CENTER);
                for (int i=0;i<mKep.nama.size();i++) {
                    input((i+1)+". Nama    :  " +mKep.nama.get(i),font2,Element.ALIGN_LEFT);
                    input("    Jabatan :  " +mKep.jabatan.get(i),font2,Element.ALIGN_LEFT);
                    input("    Tahun    :  " +mKep.tahunMulai.get(i)+" - "+mKep.tahunSelesai.get(i),font2,Element.ALIGN_LEFT);
                }
                input(" ",font1,Element.ALIGN_CENTER);
                input("--------------------------------------------------------------------------------------------------------",font2,Element.ALIGN_JUSTIFIED);
                input("RIWAYAT PENDIDIKAN",font1,Element.ALIGN_CENTER);
                input(" ",font1,Element.ALIGN_CENTER);
                for (int i=0;i<mPen.nama.size();i++) {
                    input((i+1)+". " +mPen.nama.get(i),font2,Element.ALIGN_LEFT);
                }
                input(" ",font1,Element.ALIGN_CENTER);
                input("--------------------------------------------------------------------------------------------------------",font2,Element.ALIGN_JUSTIFIED);
                input("RIWAYAT PRESTASI",font1,Element.ALIGN_CENTER);
                input(" ",font1,Element.ALIGN_CENTER);
                for (int i=0;i<mPres.nama.size();i++) {
                    input((i+1)+". " +mPres.nama.get(i),font2,Element.ALIGN_LEFT);
                }
            document.close();
            writer.close();
        } catch(DocumentException e) {
            e.printStackTrace();
        }
        JOptionPane.showMessageDialog(this, "File CV_Baru.pdf sukses dibuat di direktori "+loc,"Notification!",HEIGHT);
    }//GEN-LAST:event_jButtonCetakActionPerformed

